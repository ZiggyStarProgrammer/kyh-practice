ls = ["hej", "hopp", "hohoo", "tjohoj", "tjenixen"]

# printar listan baklänges
print(ls[::-1])

# printar varannan
print(ls[::2])

# prinar index 0
print(ls[0])

# printar varannan och börjar på index 1 i listan
print(ls[1::2])

#printar backlänges från och med index 3
print(ls[3::-1])

#printar de sista två index
print(ls[:2:-1])

